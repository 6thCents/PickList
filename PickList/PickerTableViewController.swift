import UIKit

class PickerTableViewController: UITableViewController {
    
    var selectedItem:String? {
        didSet {
            if let item = selectedItem {
                selectedItemIndex = items.items!.indexOf(item)
                items.selectedVal = item
            }
        }
    }
    var selectedItemIndex:Int?
    
    var items:PickItem!

    override func viewDidLoad() {
        super.viewDidLoad()
        
        self.navigationItem.title = items.title
        
        // set up selected default value
        selectedItem = items.selectedVal

        // Uncomment the following line to preserve selection between presentations
